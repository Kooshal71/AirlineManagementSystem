    private void EditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditBtnMouseClicked
        if(Key == 0){
            JOptionPane.showMessageDialog(this, "Select a Passenger");
        }else{
            try{
//                con = DriverManager.getConnection("jdbc:mysql://localhost/AirlineDb","root","");
                pst = con.prepareStatement("update Passengers set PName = ?, PNat = ?, PGen = ?, PPass = ?, PAdd = ?, PPhone = ? WHERE PId=?");
                pst.setString(1,PName.getText());
                pst.setString(2,PNation.getSelectedItem().toString());
                pst.setString(3,PGen.getSelectedItem().toString());
                pst.setString(4,PPass.getText());
                pst.setString(5,PAdd.getText());
                pst.setString(6,PPhone.getText());
                pst.setInt(7, Key);
                int row = pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Passenger Updated");
                PName.setText("");
                PPass.setText("");
                PAdd.setText("");
                PPhone.setText("");
                PName.requestFocus();
                update_table();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_EditBtnMouseClicked

