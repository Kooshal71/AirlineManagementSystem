    private void SaveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveBtnMouseClicked
        if(PName.getText().isEmpty() || PPass.getText().isEmpty() || PAdd.getText().isEmpty() || PPhone.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else{
            try{
//                con = DriverManager.getConnection("jdbc:mysql://localhost/AirlineDb","root","");
                pst = con.prepareStatement("insert into Passengers (PName, PNat, PGen, PPass, PAdd, PPhone) values(?,?,?,?,?,?)");
                pst.setString(1,PName.getText());
                pst.setString(2,PNation.getSelectedItem().toString());
                pst.setString(3,PGen.getSelectedItem().toString());
                pst.setString(4,PPass.getText());
                pst.setString(5,PAdd.getText());
                pst.setString(6,PPhone.getText());
                int row = pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Passenger Added");
                PName.setText("");
                PPass.setText("");
                PAdd.setText("");
                PPhone.setText("");
                PName.requestFocus();
                update_table();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_SaveBtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(Key == 0){
            JOptionPane.showMessageDialog(this, "select a passenger");
        }else{
            try {
                String Query = "Delete from Passengers where PId = "+Key;
                Statement Del;
                Del = con.createStatement();
                Del.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "passenger deleted");
                update_table();
                PName.setText("");
                PPass.setText("");
                PAdd.setText("");
                PPhone.setText("");
                PName.requestFocus();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked
    int Key = 0;
    private void PassengerTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PassengerTblMouseClicked
        DefaultTableModel model = (DefaultTableModel)PassengerTbl.getModel();
        int MyIndex = PassengerTbl.getSelectedRow();
        Key = Integer.parseInt(model.getValueAt(MyIndex, 0).toString());
        PName.setText(model.getValueAt(MyIndex, 1).toString());
        PNation.setSelectedItem(model.getValueAt(MyIndex, 2).toString());
        PGen.setSelectedItem(model.getValueAt(MyIndex, 3).toString());
        PPass.setText(model.getValueAt(MyIndex, 4).toString());
        PAdd.setText(model.getValueAt(MyIndex, 5).toString());
        PPhone.setText(model.getValueAt(MyIndex, 6).toString());
    }//GEN-LAST:event_PassengerTblMouseClicked

