    private void update_table(){
        
        try{
            St = con.createStatement();
            Rs = St.executeQuery("Select * from Bookings");
            BookingTbl.setModel(DbUtils.resultSetToTableModel(Rs));
        }
        catch(Exception e){
            System.out.println("GGS");
        }
    }
    private void GetPassengers(){
        try{
            St = con.createStatement();
            String Query = "Select * from Passengers";
            Rs = St.executeQuery(Query);
            while(Rs.next()){
                String PId = String.valueOf(Rs.getInt("PId"));
                PassId.addItem(PId);
            }
        } catch(Exception e){
            
        }
    }
    private void GetFlights(){
        try{
            St = con.createStatement();
            String Query = "Select * from Flight";
            Rs = St.executeQuery(Query);
            while(Rs.next()){
                String FlCodes = (Rs.getString("FlCode"));
                FlCode.addItem(FlCodes);
            }
        } catch(Exception e){
            
        }
    }
    private void GetPassengerData(){
        if(PassId.getSelectedIndex() != -1){
        String Query = "Select * from Passengers where PId = "+PassId.getSelectedItem().toString();
        try{
            St = con.createStatement();
            Rs = St.executeQuery(Query);
            if(Rs.next()){
                PassName.setText(Rs.getString("PName"));
                PassGen.setText(Rs.getString("PGen"));
                PassNum.setText(Rs.getString("PPass"));
                Nationality.setText(Rs.getString("PNat"));
            }
        } catch(Exception E){
            
        }
        }
        else
        {
            update_table();
        }
        
    }
