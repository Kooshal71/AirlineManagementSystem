    private void BookBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BookBtnMouseClicked
        // TODO add your handling code here:
        if(PassId.getSelectedIndex() == -1 || FlCode.getSelectedIndex() == -1 || Amount.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else{
            try{
//                con = DriverManager.getConnection("jdbc:mysql://localhost/AirlineDb","root","");
                pst = con.prepareStatement("insert into Bookings (PName, FlCode, PGen, PPass, Amount, Nationality) values(?,?,?,?,?,?)");
                pst.setString(1,PassName.getText());
                pst.setString(2,FlCode.getSelectedItem().toString());
                pst.setString(3,PassGen.getText());
                pst.setString(4,PassNum.getText());
                pst.setString(5,Amount.getText());
                pst.setString(6,Nationality.getText());
                int row = pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Ticket Booked");
                FlCode.setSelectedIndex(-1);
                PassName.setText("");
                PassNum.setText("");
                PassGen.setText("");
                Nationality.setText("");
                PassId.setSelectedIndex(-1);
                Amount.setText("");
                update_table();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_BookBtnMouseClicked

